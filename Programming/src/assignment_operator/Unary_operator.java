package assignment_operator;

public class Unary_operator 
{
	public static void main(String[] args) 
	{
		int a=9;
		System.out.println("a :"+a);
		++a;
		System.out.println("a :"+a);
		a++;
		System.out.println("a :"+a);
		a--;
		System.out.println("a :"+a);
		--a;
		System.out.println("a :"+a);
		a+=1;
		System.out.println("a :"+a);
		a-=1;
		System.out.println("a :"+a);
	}

}
